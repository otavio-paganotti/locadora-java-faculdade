    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        ArrayList<Cliente> clientes = new ArrayList<Cliente>();
        
        if (jTextFieldNome.getText().length() > 0) {
            cliente.buscar(jTextFieldNome.getText());
        } else if (jTextFieldCPF.getText().length() > 0) {
            cliente.buscarCpf(jTextFieldCPF.getText());
        }
        
        if (cliente.getListaClientes().size() == 1) {
            this.jLabelUserFound.setVisible(true);
            this.jLabelUserFound.setText("Usuário encontrado! " + cliente.getListaClientes().get(0).getNome());
        } else if (cliente.getListaClientes().size() > 1) {
            System.out.println("Tem mais de um resultado encontrado!");
        } else {
            this.jLabelUserFound.setVisible(true);
            this.jLabelUserFound.setText("Nenhum resultado encontrado.");
        }
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        cliente.removerCliente(cliente.getListaClientes().get(0));
        this.jLabelUserFound.setText(cliente.getListaClientes().get(0).getNome() + " Removido!");
        this.jTextFieldNome.setText("");
        this.jTextFieldCPF.setText("");
        cliente.setListaClientes(new ArrayList<Cliente>());
        System.out.println("Cliente Removido com sucesso!");
    }//GEN-LAST:event_jButtonExcluirActionPerformed

